package org.yesser;

public class Aritmetica {
    public double sumar(double a, double b)
    {
        return a + b;
    }

    public double restar(double a, double b)
    {
        return a - b;
    }

    public double multiplicar(double d, double e) {
        return d * e;
    }

    public double dividir(double d, double e) {
        return d / e;
    }
}